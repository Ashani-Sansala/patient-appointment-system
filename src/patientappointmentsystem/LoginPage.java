    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        /*Get the inputs for username and password from the user and check them. 
        If username or password is incorrect user cannot login. 
        Default username is "admin" and password is "admin"*/
        
        String username = un.getText();
        String password = pass.getText();
        
        if(username.equals("admin") && password.equals("admin")){
            MenuPage obj = new MenuPage();
            obj.setVisible(true);
            dispose();
        }else if(username.equals("") && password.equals("")){
            JOptionPane.showMessageDialog(rootPane,"Username or Password cannot be empty!");
        }else{
            JOptionPane.showMessageDialog(rootPane,"Username or Password is incorrect!");
        }
            
    }//GEN-LAST:event_jButton1ActionPerformed

